x = "hello python!"
print('before uppercase:', x)

#convert the element in the string uppercase
x_upper = x.upper()
print('after uppercase: ', x_upper)

#convert the element in the string lowercase
x_lower = x.lower()
print('before lowercase: ',x_lower)

#convert the title in the string
x_title = x.title()
print('this title is uppercase: ', x_title)